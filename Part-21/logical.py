# logical AND: True if the both the operands are true x and y
# logical OR: True if either of the operands is true x or y
# logical NOT: True if operand is false not x

# Condition AND
ssc = 70
hsc = 80

if ssc >= 60 and hsc >= 60:
    print("Eligible")
else:
    print("Not Eligible")

# Condition
ssc = 70
hsc = 55

if ssc >= 60 or hsc >= 60:
    print("Eligible")
else:
    print("Not Eligible")

